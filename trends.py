def get_trends(lang: str) -> str:
    # Заглушки – позже заменим на реальную аналитику
    trends_ru = [
        "📈 Умные часы (рост 27% за месяц)",
        "🔥 Мини-проекторы (в вирусных рекомендациях)",
        "🧼 Автоматические дозаторы мыла",
        "🎧 Беспроводные мини-наушники",
        "💡 Настольные лампы с RGB-подсветкой",
    ]

    trends_kg = [
        "📈 Акылдуу сааттар (айына +27%)",
        "🔥 Мини-проекторлор",
        "🧼 Автоматтык самын дозаторлору",
        "🎧 Зымсыз мини-наушниктер",
        "💡 RGB жарыгы бар лампалар",
    ]

    trends_kz = [
        "📈 Ақылды сағаттар (+27% айына)",
        "🔥 Мини-проекторлар",
        "🧼 Автоматты сабын диспенсерлері",
        "🎧 Сымсыз мини-құлаққаптар",
        "💡 RGB шамдары",
    ]

    mapping = {
        "ru": trends_ru,
        "kg": trends_kg,
        "kz": trends_kz
    }

    arr = mapping.get(lang, trends_ru)

    return "📊 *Тренды сейчас:*\n\n" + "\n".join(f"• {t}" for t in arr)
